import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');

const SOURCES = [
  {
    exportName: 'READER_BATCH_SYNC_ACCOUNT_CHILD_SOURCE',
    sourcePath: path.join(projectRoot, 'server/utils/reader-batch-sync-account-child.ts'),
  },
  {
    exportName: 'READER_BATCH_SYNC_JOB_CHILD_SOURCE',
    sourcePath: path.join(projectRoot, 'server/utils/reader-batch-sync-job-child.ts'),
  },
];

const outputPath = path.join(projectRoot, 'server/utils/runtime-child-sources.generated.ts');

function readSource(sourcePath) {
  return fs.readFileSync(sourcePath, 'utf8').replace(/\r\n/g, '\n');
}

function buildOutput() {
  const lines = ['// Generated by scripts/generate-runtime-child-sources.mjs', ''];

  for (const source of SOURCES) {
    lines.push(`export const ${source.exportName} = ${JSON.stringify(readSource(source.sourcePath))};`);
    lines.push('');
  }

  return `${lines.join('\n').trimEnd()}\n`;
}

const nextContent = buildOutput();
const currentContent = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : '';

if (currentContent !== nextContent) {
  fs.writeFileSync(outputPath, nextContent, 'utf8');
}
